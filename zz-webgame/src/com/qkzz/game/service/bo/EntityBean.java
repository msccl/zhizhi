package com.qkzz.game.service.bo;

public class EntityBean<T> {
	
	private int status;
	private T object;
	
	public int getStatus() {
		return status;
	}
	public void setStatus(int status) {
		this.status = status;
	}
	public T getObject() {
		return object;
	}
	public void setObject(T object) {
		this.object = object;
	}

}
