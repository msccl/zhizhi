package com.qkzz.im.bean;

public class AttachItem {
	private int id;
	private int attachid;//附件表ID
	private int type;//附件类型 0：道具 1：货币
	private int itemid;//根据type值选择不同的道具ID或者货币id
	private double count;//附件数量
	private String gettime;//领取时间
	private int status;//附件状态 0：尚未领取 1：已经领取
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getAttachid() {
		return attachid;
	}
	public void setAttachid(int attachid) {
		this.attachid = attachid;
	}
	public int getType() {
		return type;
	}
	public void setType(int type) {
		this.type = type;
	}
	public int getItemid() {
		return itemid;
	}
	public void setItemid(int itemid) {
		this.itemid = itemid;
	}
	public double getCount() {
		return count;
	}
	public void setCount(double count) {
		this.count = count;
	}
	public String getGettime() {
		return gettime;
	}
	public void setGettime(String gettime) {
		this.gettime = gettime;
	}
	public int getStatus() {
		return status;
	}
	public void setStatus(int status) {
		this.status = status;
	}
	
}
