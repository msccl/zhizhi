package com.qkzz.online.bean;

/**
 * 在线队列，每次操作调用该表，加入到分发队列
 * @author Administrator
 *
 */
public class OnlineQueue {
	private long id;
	private long uid;
	private String name;//用户昵称
	private long lasttime;//最后活跃时间
	private String lasturl;//最后访问游戏地址
	private int gameid;//当前正在玩的游戏ID
	private int status;//游戏在线状态值 1：全局上线 2：正在玩 3：正在打开
	private String sign;//签名
	public String getSign() {
		return sign;
	}
	public void setSign(String sign) {
		this.sign = sign;
	}
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public long getUid() {
		return uid;
	}
	public void setUid(long uid) {
		this.uid = uid;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public long getLasttime() {
		return lasttime;
	}
	public void setLasttime(long lasttime) {
		this.lasttime = lasttime;
	}
	public String getLasturl() {
		return lasturl;
	}
	public void setLasturl(String lasturl) {
		this.lasturl = lasturl;
	}
	public int getGameid() {
		return gameid;
	}
	public void setGameid(int gameid) {
		this.gameid = gameid;
	}
	public int getStatus() {
		return status;
	}
	public void setStatus(int status) {
		this.status = status;
	}

}
