package com.qkzz.web.developer.bean;

/**
 * 游戏信息
 * @author Administrator
 *
 */
public class GameInfo {
	private int id;
	private String gamecode;//游戏唯一编号
	private String name;//游戏名称
	private String intro;//游戏描述
	private String strategy;//游戏玩法
	private String logo;//游戏Logo
	private String url;//游戏地址
	private String host;//游戏Host
	private int developerid;//开发商ID
	private int status;//游戏状态 0：等待审核 1：审核通过 -1：审核未通过
	private String createtime;//创建时间
	private int width;//游戏宽度
	private int height;//游戏高度
	private String uiurl;//游戏UI地址
	
	public String getUiurl() {
		return uiurl;
	}
	public void setUiurl(String uiurl) {
		this.uiurl = uiurl;
	}
	public int getWidth() {
		return width;
	}
	public void setWidth(int width) {
		this.width = width;
	}
	public int getHeight() {
		return height;
	}
	public void setHeight(int height) {
		this.height = height;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getIntro() {
		return intro;
	}
	public void setIntro(String intro) {
		this.intro = intro;
	}
	public String getStrategy() {
		return strategy;
	}
	public void setStrategy(String strategy) {
		this.strategy = strategy;
	}
	public String getLogo() {
		return logo;
	}
	public void setLogo(String logo) {
		this.logo = logo;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getHost() {
		return host;
	}
	public void setHost(String host) {
		this.host = host;
	}
	public int getDeveloperid() {
		return developerid;
	}
	public void setDeveloperid(int developerid) {
		this.developerid = developerid;
	}
	public int getStatus() {
		return status;
	}
	public void setStatus(int status) {
		this.status = status;
	}
	public String getCreatetime() {
		return createtime;
	}
	public void setCreatetime(String createtime) {
		this.createtime = createtime;
	}
	public String getGamecode() {
		return gamecode;
	}
	public void setGamecode(String gamecode) {
		this.gamecode = gamecode;
	}
}
