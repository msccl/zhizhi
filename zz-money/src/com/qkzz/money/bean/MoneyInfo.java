package com.qkzz.money.bean;

/**
 * 游戏开发商定义的货币信息
 * @author Administrator
 *
 */
public class MoneyInfo {
	private int id;
	private String name;//货币名称
	private int canexchange;//是否可以兑换吱币 0:不可以 1:可以
	private double exchangerate;//吱币兑换比率
	private int gameid;//开发商游戏ID
	private int status;//货币状态 0：等待审核 1：审核通过 -1：审核未通过
	private String createtime;//创建时间
	private String idingame;//货币在游戏中的唯一ID
	
	public String getIdingame() {
		return idingame;
	}
	public void setIdingame(String idingame) {
		this.idingame = idingame;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getCanexchange() {
		return canexchange;
	}
	public void setCanexchange(int canexchange) {
		this.canexchange = canexchange;
	}
	public double getExchangerate() {
		return exchangerate;
	}
	public void setExchangerate(double exchangerate) {
		this.exchangerate = exchangerate;
	}
	public int getGameid() {
		return gameid;
	}
	public void setGameid(int gameid) {
		this.gameid = gameid;
	}
	public int getStatus() {
		return status;
	}
	public void setStatus(int status) {
		this.status = status;
	}
	public String getCreatetime() {
		return createtime;
	}
	public void setCreatetime(String createtime) {
		this.createtime = createtime;
	}
}
