package com.qkzz.user.service.bo;

import java.util.List;

import com.qkzz.user.bean.UserMood;


public class MoodListBean {

	private int status;			//状态
	private int start;			//页码
	private int total;			//总量
	private int count;			//每页数量
	private List<UserMood> list;//用户心情列表
	
	public int getStatus() {
		return status;
	}
	public void setStatus(int status) {
		this.status = status;
	}
	public int getStart() {
		return start;
	}
	public void setStart(int start) {
		this.start = start;
	}
	public int getTotal() {
		return total;
	}
	public void setTotal(int total) {
		this.total = total;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	public List<UserMood> getList() {
		return list;
	}
	public void setList(List<UserMood> list) {
		this.list = list;
	}
	
	
	
    
}
