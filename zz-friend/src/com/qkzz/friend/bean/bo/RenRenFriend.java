package com.qkzz.friend.bean.bo;

/**
 * 人人好友信息
 * @author dell
 *
 */
public class RenRenFriend {
	private String id;//表示好友的用户ID
	private String name;//表示好友的名字
	private String tinyurl;//标识好友小小头像
	private String headurl;//表示好友的头像
	private String headurl_with_logo;//带有校内logo的头像
	private String tinyurl_with_logo;//带有校内logo的小头像
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getTinyurl() {
		return tinyurl;
	}
	public void setTinyurl(String tinyurl) {
		this.tinyurl = tinyurl;
	}
	public String getHeadurl() {
		return headurl;
	}
	public void setHeadurl(String headurl) {
		this.headurl = headurl;
	}
	public String getHeadurl_with_logo() {
		return headurl_with_logo;
	}
	public void setHeadurl_with_logo(String headurl_with_logo) {
		this.headurl_with_logo = headurl_with_logo;
	}
	public String getTinyurl_with_logo() {
		return tinyurl_with_logo;
	}
	public void setTinyurl_with_logo(String tinyurl_with_logo) {
		this.tinyurl_with_logo = tinyurl_with_logo;
	}
	
}
